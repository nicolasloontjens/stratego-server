package be.howest.ti.stratego2021.logic;

public class Coords {
    private final int col;
    private final int row;

    public Coords(int col, int row){
        this.col = col;
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    public int getRow() {
        return row;
    }
}
